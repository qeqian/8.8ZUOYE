#include<stdint.h>
#include<stdio.h>
#include<stdlib.h>
//没学过，正在补学。 
int main()
{
	
	int n;
	uint32_t num=0;//定义所需类型的变量，初始化为零。
	// 初始化随机数生成器的种子
    // time(NULL) 获取当前系统时间（秒数），转换为无符号整数作为种子
    // 这样每次程序运行时，随机数生成结果会不同，避免重复
	srand((unsigned int)time(NULL)); 
	for(n=1;n<=32;n++)
	{
		int n=rand()%101;//生成随机数，确保在0~100之间 rand()通常生成0~32767 
		if(n>50)//判断是否大于50 
		{
			num |=1u<<(n-1);//进行位操作 
		}
	}
	printf("最终生成的uint32_t整数：%u\n",num);//以十进制输出
	         // 如果大于 50，将 result 的第 n 位设置为 1
            // 1U 表示无符号的 1，避免整数提升导致的符号问题
            // (n - 1) 是因为位操作从 0 开始计数（第 1 位对应二进制位的第 0 位）
            // 左移后，用按位或（|=）操作，将对应位设为 1，不影响其他位 
	return 0;
}
